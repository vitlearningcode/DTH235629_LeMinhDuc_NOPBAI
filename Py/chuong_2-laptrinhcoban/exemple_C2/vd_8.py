# lệnh điều kiện if...else
a = 10 
b = 20 
if a < b:
    print('a = ', a) 
    print('b = ', b) 
    print('a nhỏ hơn b') 
else:
    print('a = ', a) 
    print('b = ', b) 
    print('a lớn hơn b') 
    print('Tổng của a và b là', a + b)
    
print('--------\n')

# lệnh điều kiện if...elif...else
a = 10 
b = 10 
if a < b: 
    print('a nhỏ hơn b') 
elif a > b: 
    print('a lớn hơn b') 
else: 
    print('a bằng b') 
    
print ('--------\n')